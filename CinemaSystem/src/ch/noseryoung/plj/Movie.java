package ch.noseryoung.plj;

public class Movie {

    String movieName;
    String language;
    double duration;
    String genre;
    int age;

    public Movie(String movieName, String language, double duration, String genre, int age) {
        this.movieName = movieName;
        this.language = language;
        this.duration = duration;
        this.genre = genre;
        this.age = age;
    }


}
